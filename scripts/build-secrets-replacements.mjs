#!/usr/bin/env node
/**
 * Build secrets-replacements.txt from historical .env blobs (local only; never commit output).
 * Usage: node scripts/build-secrets-replacements.mjs > secrets-replacements.txt
 */
import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";

const KEY_PREFIXES = [
  "OPEN_ENERGY_API_KEY",
  "DMZ_DATABASE_URL",
  "DMZ_DIRECT_URL",
  "DATABASE_URL",
  "DIRECT_URL",
  "NEXT_PUBLIC_SUPABASE_URL",
  "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  "NEXT_PUBLIC_SUPABASE_PUBLISHABLE_DEFAULT_KEY",
  "VERCEL_OIDC_TOKEN",
  "GOOGLE_API_KEY",
  "GEMINI_API_KEY",
];

const HOST_FRAGMENTS = ["postgres.kcuciq", "nbpljqueahmbwgxlnkpv", "kcuciqpxxrqjmqcpulmq"];

function git(cmd) {
  return execSync(cmd, { encoding: "utf8", maxBuffer: 50 * 1024 * 1024 }).trim();
}

function commitsTouchingEnv() {
  const out = git('git log --format=%H -- .env');
  return out ? out.split("\n").filter(Boolean) : [];
}

function parseEnvBlob(text) {
  const entries = [];
  for (const line of text.split("\n")) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const m = t.match(/^([A-Za-z_][A-Za-z0-9_]*)=(.*)$/);
    if (!m) continue;
    const key = m[1];
    let value = m[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    entries.push({ key, value, raw: t });
  }
  return entries;
}

function collectLiterals() {
  const literals = new Set();
  const commits = commitsTouchingEnv();
  for (const sha of commits) {
    let blob;
    try {
      blob = git(`git show ${sha}:.env`);
    } catch {
      continue;
    }
    for (const { key, value } of parseEnvBlob(blob)) {
      if (!value || value.length < 8) continue;
      if (KEY_PREFIXES.some((k) => key === k || key.startsWith("NEXT_PUBLIC_SUPABASE"))) {
        literals.add(value);
      }
      for (const frag of HOST_FRAGMENTS) {
        if (value.includes(frag)) literals.add(value);
      }
    }
  }
  return [...literals].sort((a, b) => b.length - a.length);
}

function main() {
  const literals = collectLiterals();
  const lines = [
    "# ==============================================================================",
    "# GIT-FILTER-REPO — generated by scripts/build-secrets-replacements.mjs",
    "# ==============================================================================",
    "# DO NOT COMMIT. Review locally, then:",
    "#   git filter-repo --replace-text secrets-replacements.txt --force",
    "#   git remote add origin https://github.com/dwoods360-cyber/ironframe-live.git",
    "#   git push origin main --force",
    "# Then rotate all keys in Supabase / Vercel / Google AI Studio / Open Energy.",
    "#",
    `# Unique literal values from .env history: ${literals.length}`,
    "",
    "# --- Regex safety net (assignment lines) ---",
    'regex:OPEN_ENERGY_API_KEY=(["\']?)[^\\n"\']+\\1==>OPEN_ENERGY_API_KEY="REDACTED_OPEN_ENERGY"',
    'regex:GOOGLE_API_KEY=(["\']?)[^\\n"\']+\\1==>GOOGLE_API_KEY="REDACTED_GOOGLE"',
    'regex:GEMINI_API_KEY=(["\']?)[^\\n"\']+\\1==>GEMINI_API_KEY="REDACTED_GEMINI"',
    'regex:VERCEL_OIDC_TOKEN=(["\']?)[^\\n"\']+\\1==>VERCEL_OIDC_TOKEN="REDACTED_VERCEL_OIDC"',
    "regex:DMZ_DATABASE_URL=([^\\n]+)=>DMZ_DATABASE_URL=\"REDACTED_DMZ_DATABASE\"",
    "regex:DMZ_DIRECT_URL=([^\\n]+)=>DMZ_DIRECT_URL=\"REDACTED_DMZ_DIRECT\"",
    "regex:postgres\\.kcuciq[^\\s\"']*==>REDACTED_SUPABASE_POOLER_HOST",
    "regex:eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9\\.[^\"'\\s]+==>REDACTED_SUPABASE_JWT",
    "",
    "# --- Literal replacements (exact values from git history) ---",
  ];

  let i = 0;
  for (const value of literals) {
    const escaped = value.replace(/\\/g, "\\\\");
    lines.push(`literal:${escaped}==>REDACTED_LITERAL_${++i}`);
  }

  const outPath = resolve(process.cwd(), "secrets-replacements.txt");
  writeFileSync(outPath, `${lines.join("\n")}\n`, "utf8");
  console.error(`Wrote ${literals.length} literal rules + regex block → ${outPath}`);
  console.error("Review the file, then run git filter-repo in a maintenance window.");
}

main();
